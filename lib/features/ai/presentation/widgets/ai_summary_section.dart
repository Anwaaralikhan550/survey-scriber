      'It must be reviewed, verified, and edited by a qualified surveyor '
      'before inclusion in any professional report. The AI cannot physically '
      'inspect the property and may not accurately reflect actual conditions.';

  static const recommendations =
      'These recommendations are generated by AI based on reported issues '
      'and should be treated as suggestions only. A qualified surveyor must '
      'validate all recommendations before advising clients.';

  static const riskSummary =
      'This risk summary is AI-generated for illustrative purposes only. '
      'It does not constitute professional advice. A qualified surveyor must '
      'review and validate all risk assessments.';

  static const consistency =
      'Consistency issues identified by AI are suggestions only. Some flagged '
      'items may be intentional or context-appropriate. A qualified surveyor '
      'should review all items before making changes.';

  static const photoTags =
      'Photo tags and descriptions are AI-generated suggestions only. '
      'A qualified surveyor should verify all tags and associations.';
